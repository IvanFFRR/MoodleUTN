package models;

import java.sql.Date;

/**
 *
 * @author IVAN
 */
public class Login {
    Date fecha;
    Alumno alumno;

    public Login(Date fecha, Alumno alumno) {
        this.fecha = fecha;
        this.alumno = alumno;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

    public Alumno getAlumno() {
        return alumno;
    }

    public void setAlumno(Alumno alumno) {
        this.alumno = alumno;
    }

    @Override
    public String toString() {
        return String.format("%s - %s", fecha, alumno.nombre);
    }
    
    
}
